    <h1>What is Rainfall? </h1>
    <p>Rainfall is an event happening March 2026 to close the gender gap in tech. 
        For this event, participants will have opportunities to attend workshops, create a unique final project, network with women in STEM careers, and discover the joy of code! 
        We're affiliated with the <a href="">Athena initiative</a>, a program whose goal is to support underrepresented genders in tech. </p>
